/* MARK: Celsius to Fahrenheit conversion */
import Cocoa

// Define a constant for temperature in Celsius
let celsius = 23.4

// Convert Celsius to Fahrenheit
// Formula: (23.4°C × 9 / 5) + 32 = 74.12°F
let fahrenheit = (celsius * 9 / 5) + 32

// Print the results
// Print: Celsius: 23.4°C -> Fahrenheit: 74.12°F
print("Celcius: \(celsius)°C -> Fahrenheit: \(fahrenheit)°F")
